    // Hash Tables
    
    
        // Resolving collisons: Linear probing
    
            // In this method, If we have a collision, we try to place the
            // data in the next consecutive element in the array (wrapping
            // around to the beginning if necessary) until we find a
            // vacancy.
    
            // That way, if we don't find what we're looking for in the first
            // location, at least hopefully the element is somewhere nearby.
    
    
    

