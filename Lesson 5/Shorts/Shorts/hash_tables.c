        // A collision occurs when two pieces of data, when run through the
        // hash function, yield the same hash code.
    
        // Presumably we want to store both pieces of data in the hash table,
        // so we shouldn't simply overwrite the data that happened to be
        // placed in there first.
    
        
        // We need to find a way to get both elements into the hash table
        // while trying to preserve quick insertion and lookup.
        
    

